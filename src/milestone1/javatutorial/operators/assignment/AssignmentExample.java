package milestone1.javatutorial.operators.assignment;

public class AssignmentExample {
    public static void main(String[] args){
        int a=10;
        int b=20;

        a+=4; //a=a+4
        b-=4; //b=b-4

        System.out.println(a);
        System.out.println(b);

        System.out.println("----------------------");

        int c=10;
        c+=2; // 10+2
        System.out.println(c);
        c-=2; // 12-2
        System.out.println(c);
        c*=2; // 10*2
        System.out.println(c);
        c/=2; // 20/2
        System.out.println(c);

    }
}
